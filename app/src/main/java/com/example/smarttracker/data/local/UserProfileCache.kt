package com.example.smarttracker.data.local

import com.example.smarttracker.domain.model.User

/**
 * Контракт локального кэша профиля пользователя.
 *
 * Находится в слое data (не domain) — это деталь реализации,
 * касающаяся способа хранения данных, а не бизнес-логики.
 *
 * Реализация — [UserProfileCacheImpl] через отдельный EncryptedSharedPreferences.
 * Привязка — в AuthModule.
 *
 * Мотивация:
 * GET /user/ вызывается один раз при входе — результат сохраняется здесь.
 * Все последующие обращения к getUserInfo() возвращают кэшированный [User]
 * без сетевого запроса. Сетевой запрос делается только при обновлении профиля.
 */
interface UserProfileCache {

    /**
     * Сохраняет профиль пользователя.
     * Вызывается после успешного GET /user/ — при входе или обновлении профиля.
     */
    fun save(user: User)

    /**
     * Возвращает сохранённый профиль или null, если кэш пуст / повреждён.
     */
    fun get(): User?

    /**
     * Очищает кэш. Вызывается при выходе из аккаунта.
     */
    fun clear()
}
